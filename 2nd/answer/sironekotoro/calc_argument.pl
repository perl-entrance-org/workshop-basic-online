#!/usr/bin/env perl
use strict;
use warnings;

my $first  = shift;
my $second = shift;

print "$first + $second = " . ( $first + $second ) . "\n";
print "$first - $second = " . ( $first - $second ) . "\n";
print "$first * $second = " . ( $first * $second ) . "\n";
print "$first / $second = " . ( $first / $second ) . "\n";
